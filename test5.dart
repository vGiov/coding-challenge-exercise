void main() {
  // Passo 1: Ligue o primeiro interruptor
  print('Ligue o interruptor 1');

  // Passo 2: Vá até a sala das lâmpadas e observe qual lâmpada está acesa
  print('Lâmpada acesa: controlada pelo interruptor 1');

  // Passo 3: Volte à sala dos interruptores, desligue o primeiro interruptor e ligue o segundo interruptor
  print('Desligue o interruptor 1');
  print('Ligue o interruptor 2');

  // Passo 4: Vá até a sala das lâmpadas novamente e observe
  print('Lâmpada acesa: controlada pelo interruptor 2');

  // Passo 5: A lâmpada que não foi acesa em nenhuma das visitas é controlada pelo terceiro interruptor
  print('Lâmpada apagada em ambas as visitas: controlada pelo interruptor 3');
}
